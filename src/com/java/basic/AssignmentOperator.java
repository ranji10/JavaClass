package com.java.basic;

public class AssignmentOperator {
	
	public static void main(String args[]) {
		
		int num1 = 20;
		int num2 = 30;
		
		
		num2 = num1;
		
		System.out.println("= Output:" +num2);
		
		num2 += num1;
		System.out.println("= Output:" +num2);
		
		num2 -= num1;
		System.out.println("= Output:" +num2);
		
		num2 *= num1;
		System.out.println("= Output:" +num2);
		
		num2 /= num1;
		System.out.println("= Output:" +num2);
		
		num2 %= num1;
		System.out.println("= Outpput:" +num2);
		
	}
	

}
